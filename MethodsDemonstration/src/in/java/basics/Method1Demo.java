// Program to demonstrate no arguments and no return value, method type

package in.java.basics;

public class Method1Demo {

	public static void main(String[] args) {
		
		Method1Demo md = new Method1Demo();
		
		md.display();
		md.view();
		
	}
	
	public void display() {
		System.out.println("This is display method");
	}
	
	public void show() {
		System.out.println("This is show method");
	}
	
	public void view() {
		System.out.println("This is view method");
	}

}
